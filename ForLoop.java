package automation_testing_batch;
public class ForLoop 
{
	public static void main(String[] args) 
	{
		// print name two and 10 times
		
		for(int i=0;i<2;i++)
		{
			System.out.println("Jat");
		}
		
		for(int i=1;i<11;i++)
		{
			System.out.println("Ajit");
		}
		
		// print 0 to 10
		
		for(int i=0;i<11;i++)
		{
			System.out.println(i);
		}
		
		// print 10 to 0
		
		for(int i=10;i>=-1;i--)
		{
			System.out.println(i);
		}
		
		// print -5 to 5
		
		for(int i=-5;i<=5;i++)
		{
			System.out.println(i);
		}
		
		/*// print 0 to -minus unlimited value
		
		for(int i=0;i<=1;i--)
		{
			System.out.println(i);
		}*/
		
		// print 1 to + unlimited value
		
		for(int i=1;i>=1;i++)
		{
			System.out.println(i);
		}
	}

}
